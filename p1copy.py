# Arithmetic operators

num1 = int(input("Enter the no. : "))
num2 = int(input("Enter the no. : "))

print("Sum = ", num1 + num2)
print("Difference = ", num1 - num2)
print("Product = ", num1 * num2)
print("Division = ", num1/num2)
print("Division = ", num1//num2)

num1 = float(input("Enter the no. : "))
